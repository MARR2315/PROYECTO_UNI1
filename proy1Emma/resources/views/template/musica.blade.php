                    <h2>RAPSUSKLEI</h2>
                    <p>Rapsusklei es el nombre artístico del rapero y compositor Diego Gil Fernández, rapero y MC nacido en el barrio de La Magdalena (Zaragoza), España.

Cuenta con varios premios como el "ganador al mejor álbum de hip hop de España" en los "premios de la música independiente" y "mejor disco del año" en los "premios de la música aragonesa” además de aparecer en varios libros escolares como referencia de la poesía de nuestro siglo.

Aparte de Hip Hop ha grabado varias maquetas, discos y colaboraciones de dancehall y reggae.

Empieza a rapear con tan solo 10 años y hoy en día es uno de los artistas más importantes de la escena del Rap español y latinoamericano en general, conocido por sus letras profundas y su carismático estilo de soltar versos, es capaz de desgarrar y volver a coser los corazones de cada uno de sus oyentes, alcanzando una fusión entre surrealismo y la realidad, entre tristezas y alegrías, llegando a empatizár con su público de una manera muy peculiar tan solo con la naturalidad de su mensaje y su forma melancólica de acariciar las mieles de la escritura.</p>

