                    <p>SOBRE SU DISCOGRAFIA</p>
                    <p>En 2010 publica su primer LP en solitario, "Pandemia" ganador  al mejor álbum de hip hop de España en los "premios de la música independiente" y mejor disco del año en los premios de la música aragonesa. </p>
                    <img src="/images/rapsus5.jpg">
                    <p>2012 vuelve a sus inicios con un álbum llamado "Melancolía" (Eterno Miusik) en el que trata de mantener viva la llama de la poesía que le caracteriza,reconquistando así los corazones de sus más fieles seguidores.</p>
                     <img src="/images/rapsus2.jpg">
                     <p>2016 sale a la luz uno de sus más esperados trabajos,"Origami"(Eterno Miusik 2016) y "El niño de la selva" vuelve a poner sus cartas sobre la mesa volviendo a los orígenes con un disco cargado de alma y fuerza y con colaboraciones muy bien compactadas.</p>
                     <img src="/images/rapsus6.jpg">
                     <p>2018 Después de 23 años desde su primera referencia a nivel nacional y una larga discografía vuelve con INSANO JUICIO, un intimo EP lleno de versos profundos y referencias claras al dramatismo y la melancolía con un mensaje consciente y con ritmos tanto melódicos como devastadores.</p>
